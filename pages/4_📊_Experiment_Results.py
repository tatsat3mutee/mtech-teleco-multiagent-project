def _load_results() -> dict:
    if RESULTS_PATH.exists():
        try:
            with open(RESULTS_PATH) as f:
                data = json.load(f)
            configs = data.get("ablation_configs", {})
            if configs:
                return {k: v.get("metrics", {}) for k, v in configs.items()}
        except Exception:
            pass
    return PLACEHOLDER_RESULTS


